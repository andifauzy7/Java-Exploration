public class SimpleJava {
    public static void main(String[] args) {
        // Aghniya Rahma Y.
        String greeting = "Hello";
        String s = greeting.substring(3,5);
        System.out.println(s);

        // Aji M. Zapar.
        long angka = (long) 9.22;
        System.out.println(angka);

        // Andi F. Dewantara.
        System.out.println("Happy Coding!");
        System.out.println("Mengucapkan 'Hello World!'");

        // Fahmi Widianto.
        System.out.println("Bissmillahirahmanirrahim");
        System.out.println("Bissmillahirahmanirrahim, 'Hello world'");

        // M. Ilham Surya.
        int number1 =5;
        int number2 =7;
        System.out.println(number1+number2);

        // Rezky Wahyuda S.
        float e = (float) 11.5;
        double f = 3.53;
        System.out.println("f =" + f);

        // Salma Meldiyana.
        int a,b,c;
        a=5;
        b=3;
        c= a+b;
        System.out.print("hasil: "+c);
    }
}
